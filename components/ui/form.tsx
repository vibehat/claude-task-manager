'use client';

import * as React from 'react';
import type * as LabelPrimitive from '@radix-ui/react-label';
import { Slot } from '@radix-ui/react-slot';
import type { ControllerProps, FieldPath, FieldValues } from 'react-hook-form';
import { Controller, FormProvider, useFormContext, useFormState } from 'react-hook-form';

import { cn } from '@/libs/client/utils';
import { Label } from '@/components/ui/label';

const Form = FormProvider;

interface FormFieldContextValue<
   TFieldValues extends FieldValues = FieldValues,
   TName extends FieldPath<TFieldValues> = FieldPath<TFieldValues>,
> {
   name: TName;
}

const FormFieldContext = React.createContext<FormFieldContextValue>({} as FormFieldContextValue);

const FormField = <
   TFieldValues extends FieldValues = FieldValues,
   TName extends FieldPath<TFieldValues> = FieldPath<TFieldValues>,
>({
   ...props
}: ControllerProps<TFieldValues, TName>): React.JSX.Element => {
   return (
      <FormFieldContext.Provider value={{ name: props.name }}>
         <Controller {...props} />
      </FormFieldContext.Provider>
   );
};

const useFormField = (): {
   id: string;
   name: string;
   formItemId: string;
   formDescriptionId: string;
   formMessageId: string;
   error?: { message?: string };
   invalid: boolean;
   isDirty: boolean;
   isTouched: boolean;
} => {
   const fieldContext = React.useContext(FormFieldContext);
   const itemContext = React.useContext(FormItemContext);
   const { getFieldState } = useFormContext();
   const formState = useFormState({ name: fieldContext.name });
   const fieldState = getFieldState(fieldContext.name, formState);

   if (!fieldContext) {
      throw new Error('useFormField should be used within <FormField>');
   }

   const { id } = itemContext;

   return {
      id,
      name: fieldContext.name,
      formItemId: `${id}-form-item`,
      formDescriptionId: `${id}-form-item-description`,
      formMessageId: `${id}-form-item-message`,
      ...fieldState,
   };
};

interface FormItemContextValue {
   id: string;
}

const FormItemContext = React.createContext<FormItemContextValue>({} as FormItemContextValue);

function FormItem({ className, ...props }: React.ComponentProps<'div'>): React.JSX.Element {
   const id = React.useId();

   return (
      <FormItemContext.Provider value={{ id }}>
         <div data-slot="form-item" className={cn('grid gap-2', className)} {...props} />
      </FormItemContext.Provider>
   );
}

function FormLabel({
   className,
   ...props
}: React.ComponentProps<typeof LabelPrimitive.Root>): React.JSX.Element {
   const { error, formItemId } = useFormField();

   return (
      <Label
         data-slot="form-label"
         data-error={!!error}
         className={cn('data-[error=true]:text-destructive-foreground', className)}
         htmlFor={formItemId}
         {...props}
      />
   );
}

function FormControl({ ...props }: React.ComponentProps<typeof Slot>): React.JSX.Element {
   const { error, formItemId, formDescriptionId, formMessageId } = useFormField();

   return (
      <Slot
         data-slot="form-control"
         id={formItemId}
         aria-describedby={
            !error ? `${formDescriptionId}` : `${formDescriptionId} ${formMessageId}`
         }
         aria-invalid={!!error}
         {...props}
      />
   );
}

function FormDescription({ className, ...props }: React.ComponentProps<'p'>): React.JSX.Element {
   const { formDescriptionId } = useFormField();

   return (
      <p
         data-slot="form-description"
         id={formDescriptionId}
         className={cn('text-muted-foreground text-sm', className)}
         {...props}
      />
   );
}

function FormMessage({ className, ...props }: React.ComponentProps<'p'>): React.JSX.Element | null {
   const { error, formMessageId } = useFormField();
   const body = error ? String(error?.message ?? '') : props.children;

   if (!body) {
      return null;
   }

   return (
      <p
         data-slot="form-message"
         id={formMessageId}
         className={cn('text-destructive-foreground text-sm', className)}
         {...props}
      >
         {body}
      </p>
   );
}

export {
   useFormField,
   Form,
   FormItem,
   FormLabel,
   FormControl,
   FormDescription,
   FormMessage,
   FormField,
};
